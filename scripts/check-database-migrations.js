function checkTablesExist() {
  console.log("\nüóÉÔ∏è  –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è —Ç–∞–±–ª–∏—Ü...");

  const tables = ["users", "categories", "products", "parsing_sessions"];
  const missingTables = [];

  for (const table of tables) {
    const result = runCommand(`npx prisma db execute --stdin`, {
      input: `SELECT EXISTS (SELECT FROM information_schema.tables WHERE table_name = '${table}');`,
    });

    if (result.success) {
      const exists =
        result.output.includes("t") || result.output.includes("true");
      if (exists) {
        console.log(`‚úÖ –¢–∞–±–ª–∏—Ü–∞ ${table} —Å—É—â–µ—Å—Ç–≤—É–µ—Ç`);
      } else {
        console.log(`‚ùå –¢–∞–±–ª–∏—Ü–∞ ${table} –ù–ï —Å—É—â–µ—Å—Ç–≤—É–µ—Ç`);
        missingTables.push(table);
      }
    } else {
      console.log(`‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ —Ç–∞–±–ª–∏—Ü—ã ${table}:`);
      console.log(result.error);
      missingTables.push(table);
    }
  }

  return missingTables;
}

